package com.netcoremessenger.core.data.local.entity

import androidx.room.ColumnInfo
import androidx.room.Entity
import androidx.room.ForeignKey
import androidx.room.Index

@Entity(
    tableName = "message_status",
    primaryKeys = ["message_id", "user_id"],
    foreignKeys = [
        ForeignKey(entity = MessageEntity::class, parentColumns = ["id"], childColumns = ["message_id"], onDelete = ForeignKey.CASCADE),
        ForeignKey(entity = UserEntity::class, parentColumns = ["id"], childColumns = ["user_id"], onDelete = ForeignKey.CASCADE)
    ],
    indices = [Index("message_id"), Index("user_id")]
)
data class MessageStatusEntity(
    @ColumnInfo(name = "message_id") val messageId: Long,
    @ColumnInfo(name = "user_id") val userId: Long,
    val status: String,


    @ColumnInfo(name = "status_at") val statusAt: Long
)










































































































































































































































































































































